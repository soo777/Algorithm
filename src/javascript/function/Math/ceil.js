/* 
    Math.ceil()
    주어진 숫자보다 크거나 같은 숫자 중 가장 작은 숫자를 Integer로 반환.
*/

let value;

value = Math.ceil(.95);    // 1
console.log(value);

value = Math.ceil(4);      // 4
console.log(value);

value = Math.ceil(7.004);  // 8
console.log(value);

value = Math.ceil(-0.95);  // -0
console.log(value);

value = Math.ceil(-4);     // -4
console.log(value);

value = Math.ceil(-7.004); // -7
console.log(value);

